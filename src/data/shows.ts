function makeReactions(fireCount: number, heartCount: number, others?: Partial<Record<string, number>>): Reaction[] {
  return [
    { type: 'fire', count: fireCount, userReacted: false },
    { type: 'heart', count: heartCount, userReacted: false },
    { type: 'guitar', count: others?.guitar ?? Math.floor(Math.random() * 20), userReacted: false },
    { type: 'mindblown', count: others?.mindblown ?? Math.floor(Math.random() * 15), userReacted: false },
    { type: 'dancing', count: others?.dancing ?? Math.floor(Math.random() * 25), userReacted: false },
    { type: 'clap', count: others?.clap ?? Math.floor(Math.random() * 10), userReacted: false },
  ];
}

